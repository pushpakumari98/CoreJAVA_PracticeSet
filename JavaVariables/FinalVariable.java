package JavaVariables;

public class FinalVariable {
    public static void main(String[] args) {
        
        final float PI=3.14F;
        System.out.println(PI);
    
        //final or  constants is a variable in java which has a fixed value ,it cann't be assigned a diffrent value once assigned.
        final float UniversalGasConstant=8.314f;
        System.out.println(UniversalGasConstant);

        final String SpeedOfLight="3*10^8";
        System.out.println(SpeedOfLight);

    }  
    
}
