public class PatternPrinting {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        for (int j = 1; j <= 4; j++) {
            System.out.print(j + " ");
        }
        System.out.println(1);
        for (int k = 1; k <= 3; k++) {
            System.out.print(k + " ");
        }
        System.out.print(1 + " " + 2);
        System.out.println();
        for (int l = 1; l <= 2; l++) {
            System.out.print(l + " ");
        }
        for (int m = 1; m <= 3; m++) {
            System.out.print(m + " ");
        }
    }
}
